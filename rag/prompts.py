# 渲染缓存：(语料 slug, 模板名) → 已注入文本
_RENDER_CACHE: dict = {}


def __getattr__(name: str) -> str:
    """PEP 562：访问 _RAW_TEMPLATES 中的公开模板名时，注入激活语料的书名/背景。

    用 str.replace 而非 .format，模板内运行时占位符（{query} 等）无需双层转义。
    """
    raw = _RAW_TEMPLATES.get(name)
    if raw is None:
        raise AttributeError(f"module {__name__!r} has no attribute {name!r}")
    from rag.corpus import get_active_profile  # 延迟导入，避免 import 期加载语料
    profile = get_active_profile()
    key = (profile.slug, name)
    if key not in _RENDER_CACHE:
        _RENDER_CACHE[key] = (
            raw.replace("{book_title}", profile.title)
               .replace("{corpus_context}", profile.context)
        )
    return _RENDER_CACHE[key]


